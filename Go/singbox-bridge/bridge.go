// ---------- start ----------

//export bridge_start_with_config
func bridge_start_with_config(
	socks_port C.int32_t,
	dns_port C.int32_t,
	controller_addr *C.char,
	secret *C.char,
	config_json *C.char,
) C.int32_t {
	mu.Lock()
	defer mu.Unlock()

	if running {
		setError("proxy is already running")
		return -1
	}

	if config_json == nil {
		setError("config_json is null")
		return -1
	}

	configBytes := []byte(C.GoString(config_json))

	// Parse JSON config using sing-box's own JSON library
	ctx := context.Background()
	ctx = box.Context(
		ctx,
		include.InboundRegistry(),
		include.OutboundRegistry(),
		include.EndpointRegistry(),
	)
	var opts option.Options
	if err := json.UnmarshalContext(ctx, configBytes, &opts); err != nil {
		setError(fmt.Sprintf("parse config: %v", err))
		return -1
	}

	// The JSON generated by Swift already contains the correct ports and clash_api config.
	// We just need to store them for the getter functions.
	sp := int(socks_port)
	dp := int(dns_port)
	ctrlAddr := ""
	if controller_addr != nil {
		ctrlAddr = C.GoString(controller_addr)
	}

	// Create and start sing-box instance
	runCtx, cancel := context.WithCancel(ctx)
	inst, err := box.New(box.Options{
		Context: runCtx,
		Options: opts,
	})
	if err != nil {
		cancel()
		setError(fmt.Sprintf("box.New: %v", err))
		return -1
	}
	if err := inst.Start(); err != nil {
		cancel()
		setError(fmt.Sprintf("box.Start: %v", err))
		return -1
	}

	instance = inst
	cancelFn = cancel
	running = true
	cachedSocksPort = int32(sp)
	cachedDNSPort = int32(dp)
	cachedCtrlAddr = ctrlAddr

	runtime.GC()
	debug.FreeOSMemory()
	return 0
}

// ---------- stop ----------

//export bridge_stop_proxy
func bridge_stop_proxy() {
	mu.Lock()
	defer mu.Unlock()

	if !running {
		return
	}
	if cancelFn != nil {
		cancelFn()
		cancelFn = nil
	}
	if instance != nil {
		_ = instance.Close()
		instance = nil
	}
	running = false
	cachedSocksPort = 0
	cachedDNSPort = 0
	cachedCtrlAddr = ""
	runtime.GC()
	debug.FreeOSMemory()
}

