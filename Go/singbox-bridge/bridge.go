// Validate config JSON (parse-only, no start)
//export bridge_validate_config
func bridge_validate_config(config_json *C.char) C.int32_t {
	if config_json == nil {
		setError("config_json is null")
		return -1
	}
	configBytes := []byte(C.GoString(config_json))
	ctx := box.Context(
		context.Background(),
		include.InboundRegistry(),
		include.OutboundRegistry(),
		include.EndpointRegistry(),
	)
	var opts option.Options
	if err := json.UnmarshalContext(ctx, configBytes, &opts); err != nil {
		setError(fmt.Sprintf("invalid config: %v", err))
		return -1
	}
	return 0
}

